export class CommonMisspelled implements LinterInterface {
  type = LinterType.WORD;

  words: any[];

  constructor(words = misspelledWords) {
    this.words = words;
  }

  run(word: String) {
    const found = this.words.find(pair => pair[0] === word);
    if (found) {
      return [{ message: `Може би имахте предвид "${found[1]}"` }];
    }
    return [];
  }
}
